package model;

public class TeacherBean {
	String tuser;
	String tpsd;
	String tmailbox;
	String tschool;
	String tname;
	int tid;
	public String getTuser() {
		return tuser;
	}
	public void setTuser(String tuser) {
		this.tuser = tuser;
	}
	public String getTpsd() {
		return tpsd;
	}
	public void setTpsd(String tpsd) {
		this.tpsd = tpsd;
	}
	public String getTmailbox() {
		return tmailbox;
	}
	public void setTmailbox(String tmailbox) {
		this.tmailbox = tmailbox;
	}
	public String getTschool() {
		return tschool;
	}
	public void setTschool(String tschool) {
		this.tschool = tschool;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
}
